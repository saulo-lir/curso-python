# Soma

n1 = 7
n2 = 2

soma = n1+n2
print(soma)

# Subtração

subtracao = n1-n2
print(subtracao)

# Multiplicação

mult = n1*n2
print(mult)

# Divisão

divisao = n1/n2
print(divisao)

# Divisão Inteira

divisao = n1//n2 # Retorna a parte inteira da divisão
print(divisao)

divisao = n1%n2 # Retorna o resto da divisão
print(divisao)

# Exponenciação

expo = 2**3
print(expo)

'''
1. Parenteses: ()
2. Potências: **
3. Multiplicação, divisão, divisão inteira e o resto da divisão: *, /, //, %
4. Soma, subtração: +, -
'''